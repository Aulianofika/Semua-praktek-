public class contohwhile {
    
    public static void main(String[] args)
    {  
        int c = 0;
        while(c < 10){
            System.out.print(c+" ");
            c++;
        }
        c=0;
        do{
            System.out.println(c+" ");
            c++;
        }while (c < 10);
        
            
        }
        
    
}
    

